public class Animal {
    private String name;
    private int kgOfFood;

    public String getName() {
        return name;
    }

    public int getKgOfFood() {
        return kgOfFood;
    }

    public Animal(String name, int kgOfFood) {
        this.name = name;
        this.kgOfFood = kgOfFood;
    }
}
